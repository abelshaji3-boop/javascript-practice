// Create an array from 1 to 20

let a1 = []                     // empty array

for(let i = 1; i <= 20; i++){   // loop from 1 to 20
    a1.push(i)                  // push the value of i into the array
}

console.log(a1)                 // print the array


// NOTES:

// 1. [] creates an empty array.
// 2. push() adds elements to the end of the array.
// 3. The for loop runs from 1 to 20.
// 4. Each value of i is added to the array.
// 5. Final array will contain numbers from 1 to 20.