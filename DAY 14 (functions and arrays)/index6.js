// Array

let students = ["abel","agnivesh","adithyan","sonu","bobin"]   // array storing multiple values

console.log(students)        // prints the entire array

console.log(students[0])     // prints the first element of the array


// Array Methods - length

console.log(students.length) // returns the total number of elements in the array


// NOTES:

// 1. An array is used to store multiple values in one variable.
// 2. Elements are stored inside square brackets [].
// 3. Array indexing starts from 0.
// 4. students.length returns the size of the array.
// 5. In this example length = 5 because there are 5 names.