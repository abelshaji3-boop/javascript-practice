//do-while loop

let num = 0;   // variable initialization

do {
    console.log("hello");   // code executes once
} while (num > 0);          // condition checked after execution


// NOTE:
// do-while runs the code first, then checks the condition.
// So it will execute at least one time.

// COMPARISON
// while → checks condition first
// do-while → executes first, then checks condition