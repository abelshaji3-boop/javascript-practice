//using do-while loop print the first 10 multipke of 7

let i = 1;        // start from 1
let num = 7;      // number whose multiples we want

do {
    console.log(num * i);   // print multiple of 7
    i++;                    // increase counter
} while (i <= 10);          // run until 10 multiples are printed


// NOTE:
// do-while loop executes the code first and checks the condition later.
// This program prints the first 10 multiples of 7.