// Immediately Invoked Function Expression (IIFE)

(() => {
    console.log("hello world")   // code inside the function runs immediately
})()                              // () at the end invokes (calls) the function


// NOTES:

// 1. IIFE means Immediately Invoked Function Expression.
// 2. The function runs immediately after it is defined.
// 3. The function is wrapped inside () to make it an expression.
// 4. The last () calls the function instantly.
// 5. IIFE is used when we want to execute a function only once.