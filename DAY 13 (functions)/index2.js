// Anonymous Function

// syntax:
// () => {
//   block of code
// }

() => {
    console.log("hello world")
}


// NOTES:

// 1. Anonymous function means a function without a name.
// 2. It is usually used inside variables, callbacks, or events.
// 3. In this example, the function prints "hello world".
// 4. Since it has no name, it cannot be called directly unless stored in a variable.
// 5. Anonymous functions are commonly used with arrow functions in modern JavaScript.