// function declaration

function checkeven(){          // defining a function named checkeven
    let num = 20               // variable storing number

    if(num % 2 == 0){          // check if number is divisible by 2
        console.log("even")    // if true → number is even
    }else{
        console.log("odd")     // otherwise → number is odd
    }
}

// function calling

checkeven()   // calling the function
checkeven()   // calling again
checkeven()   // calling again