// Convert string to array using split()

let word = "malayalam"     // string

let arr = word.split('')   // split each character into array

console.log(arr)           // print array


// NOTES:

// 1. split('') converts a string into an array of characters.
// 2. '' means split at every character.
// 3. It returns a new array.
// 4. Original string is not changed.