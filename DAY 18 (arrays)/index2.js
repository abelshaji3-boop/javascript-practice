// Print last character of a string

let word = "malayalam"        // string

let length = word.length      // total number of characters

console.log(word[length - 1]) // prints last character


// NOTES:

// 1. length gives total characters in the string.
// 2. Index starts from 0.
// 3. Last index = length - 1.
// 4. word[length - 1] → last character.