// Iterate string using for...of

let word = "malayalam"     // string

for(const element of word){   // loop through each character
    console.log(element)      // print each character
}


// NOTES:

// 1. Strings are iterable in JavaScript.
// 2. for...of loop is used to access each character directly.
// 3. 'element' represents each character in the string.
// 4. It prints characters one by one.